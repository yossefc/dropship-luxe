// Helper functions
export function getProductsByCategory(categoryId: string): FallbackProduct[] {
  return fallbackProducts.filter(p => p.categoryId === categoryId);
}

export function getProductsBySubCategory(subCategoryId: string): FallbackProduct[] {
  return fallbackProducts.filter(p => p.subCategoryId === subCategoryId);
}

export function getFeaturedProducts(count: number = 8): FallbackProduct[] {
  return fallbackProducts
    .sort((a, b) => b.importScore - a.importScore)
    .slice(0, count);
}

export function getBestSellers(count: number = 8): FallbackProduct[] {
  return fallbackProducts
    .filter(p => p.badge === 'bestseller')
    .slice(0, count);
}

export function getNewArrivals(count: number = 8): FallbackProduct[] {
  return fallbackProducts
    .filter(p => p.badge === 'new')
    .slice(0, count);
}

export function getProductBySlug(slug: string): FallbackProduct | undefined {
  return fallbackProducts.find(p => p.slug === slug);
}
